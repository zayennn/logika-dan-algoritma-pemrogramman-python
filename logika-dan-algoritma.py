# pengkondisian / if else
angka = 10

if angka < 3 :
    print('angka kurang dari 3')
else :
    print('angka kamu memenuhi persyatatan')
    
# cara baca nya :
# kamu punya angka nilai nya 10
# terus di kondisiin, jika ( if ) angka ( angka kamu (10) ) nilai nya kurang dari 3 , maka jalanin yang ada di bawah nya
# kalo angka kamu (10) , nilai nya tidak ngelebihin nilai persyaratan (3) , maka tampiin yang ada dibawah nya




